/**
 * @param {string[]} rowStrings
 */
export function parseTilesFromRows(rowStrings) {
  return rowStrings.map((row) =>
    row.split("").map((ch) => (ch === "#" ? 1 : 0)),
  );
}

/**
 * Deep-clone tile grid for runtime mutation (e.g. boss arena cover).
 * @param {string[]} rowStrings
 */
export function cloneTilesFromRows(rowStrings) {
  return parseTilesFromRows(rowStrings).map((row) => [...row]);
}

/**
 * @param {number[][]} tiles
 */
export function buildTileMapFromTiles(tiles) {
  return new TileMap(TILE_SIZE, tiles);
}

/**
 * @param {number} tx
 * @param {number} ty
 */
export function tileCenter(tx, ty) {
  return {
    x: (tx + 0.5) * TILE_SIZE,
    y: (ty + 0.5) * TILE_SIZE,
  };
}

/**
 * @param {LevelDef} def
 */
export function buildTileMap(def) {
  const tiles = parseTilesFromRows(def.rows);
  return new TileMap(TILE_SIZE, tiles);
}

/**
 * Portal AABB in world pixels.
 * @param {{ tx: number; ty: number; tw: number; th: number }} p
 */
export function portalWorldRect(p) {
  return {
    x: p.tx * TILE_SIZE,
    y: p.ty * TILE_SIZE,
    w: p.tw * TILE_SIZE,
    h: p.th * TILE_SIZE,
  };
}
